                  <h3 contenteditable="true">7.National Recognition</h3>
                  <p>a. NCEAC shall be a nationally and internationally recognized body for
                    Higher Education Accreditation of Computing in Pakistan. It shall be responsible
                    for the accreditation of educational programs leading to degrees in the
                    computing discipline.&nbsp;
                    <br>
                    <br>b. Its endorsement and certification shall be valued and drawn on by&nbsp;professional
                    and technical societies, by employers, and by the institutions themselves
                    for self-appraisal and improvement.&nbsp;
                    <br>
                    <br>c. NCEAC shall run and execute the accreditation programs on behalf of
                    HEC under its guidance and instructions.</p>
