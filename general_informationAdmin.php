                <div class="well">
                  <h2></h2>
                  <h3>1. What is Accreditation?</h3>
                  <p>&nbsp;In the emerging age of information and knowledge, it is imperative
                    to ensure quality and purposefulness of our educational programs. Computing
                    professions, although young, are undoubtedly playing pivotal role in the
                    development of nations. The computing education in Pakistan being a recent
                    phenomenon needs direction, guidance, and mechanisms to achieve quality
                    standards. Accreditation processes are considered an effective instrument
                    to achieve these goals. Computing has emerged as a major academic discipline
                    and a professional field in Pakistan in the recent past. A large number
                    of educational institutions offer degree programs in computing related
                    areas both in public and private sectors. It has therefore become essential
                    that an internationally acceptable and industrially viable set of criteria
                    maybe evolved for various degree programs in computing related degree awarding
                    institutions. For this purpose, it is proposed that an accreditation authority
                    be setup which would periodically evaluate, scrutinize, and monitor the
                    standards followed in different degree awarding institutions and their
                    affiliated colleges. This authority shall be termed as the National Computing
                    Education Accreditation Council (NCEAC). The accreditation is to be used
                    to assure quality in computing degree programs in educational institutions.
                    It would require an educational institution or program to meet certain
                    defined standards or criteria. The accreditation will be for specific degree
                    programs and not institutions. Moreover, accreditation is not to be confused
                    with certification. In general, institutions and programs are accredited,
                    and individuals are certified. Accreditation protects and promotes the
                    interest of all stake holders, namely parents, students, faculty, academic
                    administrators, employers, and taxpayers. It serves to notify: Parents
                    and prospective students that a program has met minimum standards; Faculty,
                    deans and administrators of a program’s strengths and weaknesses and of
                    ways to improve the program; Employers that graduates are prepared to begin
                    professional practice; Taxpayers that their funds are spent well; and The
                    public that graduates are aware of public health and safety considerations.
                    &nbsp;</p>
                </div>
                <div class="well">
                  <h3 contenteditable="true">2.Establishment of the Council</h3>
                  <p>Whereas, Higher Education Commission was constituted on 11th September,
                    2002 by the promulgation of Ordinance F. No. 2(1)/2002.Pub And Whereas
                    the Higher Education Commission has been authorized in article 3 Sub-Section
                    (e) to set up national or regional evaluation councils or authorize any
                    existing council or similar body to carry out accreditation of Institutions
                    including their departments, faculties and disciplines by giving them appropriate
                    ratings. The Commission shall help build capacity of existing councils
                    or bodies in order to enhance the reliability of the evaluation carried
                    out by them: and Whereas, now there is an urgent need for the constitution
                    of the National Computing Education Accreditation Council (NCEAC) for the
                    smooth and the efficient working of the affairs of the Higher Education
                    Commission regarding the accreditation of institutions and their departments,
                    faculties and disciplines by giving them appropriate ratings, now therefore,
                    the said council by the name of National Computing Education Accreditation
                    Council (NCEAC) is established with immediate effect with the following,
                    organization, objectives, functions and duties to perform:</p>
                </div>
                <div class="well">
                  <h3 contenteditable="true">3.Functions of the Council</h3>
                  <ol class="text-justify">
                    <li>To organize and carry out a comprehensive program of accreditation of
                      computing programs leading to degrees</li>
                    <li>The NCEAC shall propose policies, procedures, and criteria or may suggest
                      changes or revisions of the same for Accreditation.</li>
                    <li>The NCEAC shall administer the accreditation process based on the approved
                      policies, procedures, and criteria.</li>
                    <li>The NCEAC shall approve the list of evaluators to participate in the process
                      of accreditation of academic programs.</li>
                    <li>The NCEAC shall constitute Accreditation Inspection Council (AIC) for
                      the evaluation of relevant academic programs for accreditation. AIC (to
                      be constituted from the approved list of evaluators) shall make recommendations
                      to NCEAC. The final decision of the accreditation or necessary action will
                      be made by NCEAC.</li>
                    <li>Procedures and decisions on all appeals with respect to accreditation
                      actions shall be the responsibility of the NCEAC.</li>
                    <li>Accreditation decisions will be based exclusively on the suitable criteria,
                      policies, and procedures as approved by NCEAC.</li>
                    <li>To publish a list of Recognized Computing Qualifications/programs</li>
                    <li>To promote intellectual development and understanding of subject areas
                      that impact accreditation activities in the computing profession;</li>
                    <li>To collect, index and publish information on any or all aspects of computing,
                      its teaching applications, its applications and uses to industry and to
                      maintain or support any library, bureau, database or information system
                      conducive to this end</li>
                    <li>To select program evaluators</li>
                    <li>To train and assess program evaluators</li>
                    <li>To assist academic institutions in planning their educational programs
                      for accreditation</li>
                    <li>To identify to the public, prospective students, student counselors, parents,
                      educational institutions, professional societies, potential employers,
                      governmental agencies, and state licensing or certification</li>
                    <li>To provide guidance for the improvement of the existing educational programs
                      and development of future programs leading to the computing profession.</li>
                    <li>To stimulate the improvement of computing education in Pakistan.</li>
                    <li>To develop Accreditation Policies and Processes</li>
                    <li>To develop Guidelines and Procedures for Program Evaluators</li>
                    <li>To develop Guidelines and Procedures for Programs and Institutions</li>
                    <li>To develop Guidelines for Selection of Evaluators</li>
                    <li>To identify and develop Guidelines for Ensuring “No Conflict of Interest”</li>
                    <li>To develop Program Evaluator’s Training Manual Self Study Questionnaire</li>
                    <li>To develop Forms and Templates</li>
                    <li>Collect information and statistics on accreditation of higher education
                      of Computing and its respective Institutions as it may deem fit and may
                      cause it published</li>
                  </ol>
