                <div class="well">
                  <h3 contenteditable="true">5.Accreditation of Computing Programs</h3>
                  <p class="text-justify" contenteditable="true">a) &nbsp;Accreditation shall be a mandatory process for all relevant academic
                    programs offered by public and private sector institutions. The incentive
                    for obtaining &nbsp; &nbsp; such accreditation shall be enhanced recognition
                    in the computing community and prospective students.
                    <br>b) &nbsp;All institutions in Pakistan which grant a Recognized Computing
                    Qualification must apply to the Council to have such qualification accredited.
                    <br>c) Accreditation will be carried under the umbrella of HEC and via the
                    National Computing Education Accreditation Council.
                    <br>d) The Council shall publish a list of Accredited Programs as prescribed
                    <br>e) The Recognized Computing Qualifications granted by institutions recognized
                    by the respective authorized bodies outside Pakistan shall also be recognized
                    by the Council as per need in case of Pakistani graduates of Computing
                    program
                    <br>f) Every institution in Pakistan which has an Accredited Program shall
                    furnish such information as the Council may, from time to time, require
                    as to the courses of study and examination to be undergone in order to
                    obtain such Recognized Computing Qualification
                    <br>g) The Council shall appoint such number of Program Evaluators as it may
                    deem requisite to attend at any or all of the examinations held by the
                    institutions in Pakistan for the purpose of granting Recognized Computing
                    Qualifications which are Accredited or in respect of which Accreditation
                    has been sought</p>
                </div>
                <div class="well">
                  <h3 contenteditable="true" class="text-justify">6.Institutions</h3>
                  <p class="text-justify">Programs will be considered for accreditation if they are offered by an
                    institution of higher learning in one of the following categories:
                    <br>
                    <br>a. Institutions chartered by Federal or Provincial governments
                    <br>
                    <br>&nbsp;b. Institutions affiliated by the chartered universities or degree
                    awarding institutions
                    <br>
                    <br>&nbsp;c. Institutions offering degree programs under affiliation/collaboration
                    with foreign universities under the approval from HEC.
                    <br>
                    <br>d. Institutions that operate a branch campus under the direct supervision
                    and control of the main campus, and conduct program that is substantially
                    equivalent to the one located on the main campus
                    <br>
                    <br>&nbsp;e. When a multi-campus institution presents programs for accreditation,
                    each campus will be considered as a separate institution in the evaluation
                    process.</p>
                </div>
