                  <h3 contenteditable="true">4. Scope</h3>
                  <p class="text-justify">The degree programs of the following disciplines of Computing shall fall
                    in the purview of the proposed accreditation: Computer Science Information
                    Systems Software Engineering Information Technology Bioinformatics Additional
                    emerging disciplines and degree programs pertaining to the Computing shall
                    be added to this list in the future.</p>
